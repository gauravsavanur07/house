    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        if(f instanceof Fixed){
            f = new Fixed();
            f.setOwner(ownerTf.getText());
            f.setType(typeCb.getSelectedItem().toString());
            f.setCounty(countyCb.getSelectedItem().toString());
            f.setBedrooms(Integer.parseInt(bedroomTf.getText()));
            f.setBathrooms(Integer.parseInt(bathroomTf.getText()));
            ((Fixed)f).setFixed_price(Double.parseDouble(priceTf.getText()));
            
            fList.add(f);
        }
        
        if(m instanceof Mortgage){
            m = new Mortgage();
            m.setOwner(ownerTf.getText());
            m.setType(typeCb.getSelectedItem().toString());
            m.setCounty(countyCb.getSelectedItem().toString());
            m.setBedrooms(Integer.parseInt(bedroomTf.getText()));
            m.setBathrooms(Integer.parseInt(bathroomTf.getText()));
            ((Mortgage)m).setMortgage_price(Double.parseDouble(priceTf.getText()));
            ((Mortgage)m).setYears(Integer.parseInt(yearsTf.getText()));
            
            m.compute();
            
            mList.add(m);
        }
    }//GEN-LAST:event_addBtnActionPerformed

    private void mviewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mviewBtnActionPerformed
        for(Mortgage i:mList){
            if(i instanceof Mortgage){
                JOptionPane.showMessageDialog(null, "Owner: " + i.getOwner() + "\n Type: " + i.getType() + "\n County: " + i.getCounty() + "\n Bedrooms: " + i.getBedrooms() + "\n Bathrooms: " + i.getBathrooms() + "\n Mortgage Price: €" + ((Mortgage) i).getTotal() + " Per Year");
            }
        }
    }//GEN-LAST:event_mviewBtnActionPerformed


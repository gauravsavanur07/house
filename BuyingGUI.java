    private void fviewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fviewBtnActionPerformed
        for(Fixed i:fList){
            if(i instanceof Fixed){
                JOptionPane.showMessageDialog(null, "Owner: " + i.getOwner() + "\n Type: " + i.getType() + "\n County: " + i.getCounty() + "\n Bedrooms: " + i.getBedrooms() + "\n Bathrooms: " + i.getBathrooms() + "\n Fixed Price: €" + ((Fixed) i).getFixed_price());
            }
        }
    }//GEN-LAST:event_fviewBtnActionPerformed

